package in.blogspot.randomcompiler.print.reverse.string;

public class PrintReverseStringDemo {
    public static void main(String[] args) {
        PrintReverseString.printReverseString("ABHISHEK", 0);
        System.out.println();
        
        PrintReverseString.printReverseString("Welcome", 0);
        System.out.println();
        
        PrintReverseString.printReverseString("Hello", 12);
        System.out.println();
        
        PrintReverseString.printReverseString(null, 0);
        System.out.println();
        
        PrintReverseString.printReverseString("United States of America", 1);
        System.out.println();
    }
}
